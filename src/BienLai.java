public class BienLai {
    public int SDC,SDM,STtra;

    public BienLai(int SDC, int SDM) {
        this.SDC = SDC;
        this.SDM = SDM;
        this.STtra = STtra;
    }

    public BienLai() {

    }

    public int getSDC() {
        return SDC;
    }

    public void setSDC(int SDC) {
        this.SDC = SDC;
    }

    public int getSDM() {
        return SDM;
    }

    public void setSDM(int SDM) {
        this.SDM = SDM;
    }

    public int getSTtra() {
        return STtra;
    }

    public void setSTtra(int STtra) {
        this.STtra = STtra;
    }


}
